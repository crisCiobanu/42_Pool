#include "myinc.h"

int	ft_checkop(char *str)
{
	int		i;
	char	tab[5];

	tab[0] = '+';
	tab[1] = '-';
	tab[2] = '*';
	tab[3] = '/';
	tab[4] = '%';
	if (ft_strlen(str) != 1)
		return (-1);
	i = 0;
	while (tab[i])
	{
		if (tab[i] == str[0])
			return (i);
		i++;
	}
	return (-1);
}
