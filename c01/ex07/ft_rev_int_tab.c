void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	t;
	int	tmp;

	i = 0;
	t = size - 1;
	while (i < size / 2)
	{
		tmp = tab[t];
		tab[t] = tab[i];
		tab[i] = tmp;
		i++;
		t--;
	}	
}
