int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb == 0 || nb == 1)
		return (nb);
	if (nb > 0)
	{
		while (i * i <= nb && i <= 46340)
		{
			if (i * i == nb)
				return (i);
			else
				i++;
		}
	}
	return (0);
}
